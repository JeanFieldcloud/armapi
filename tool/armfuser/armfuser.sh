#!/bin/bash

########################################################################
#
# - Script to convert c files to cpp files.
# - Date : 27.10.2016
#
########################################################################

########################################################################
# Build armfuser and run
########################################################################
make
make run

########################################################################
# Copy other files
########################################################################
cp ../../bindings/c/armconfig.h ../../bindings/cpp/armconfig.h
cp ../../bindings/c/armconst.h ../../bindings/cpp/armconst.h
cp ../../bindings/c/mem.c ../../bindings/cpp/mem.cpp

########################################################################
# Clean armfuser
########################################################################
make clean

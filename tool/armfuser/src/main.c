entity_t* makeTreeFromFile(char* fileName)
{
	//Open and parse file
	yyin = fopen(fileName, "r");
	int errParse = yyparse();
	fclose(yyin);
	
	if(errParse)
	{
		printf("In file:\"%s\" -> Parse fail!\n", fileName);
		return NULL;
	}
	
	printf("In file:\"%s\" -> Parse success!\n", fileName);
	
	entity_t* entRoot = entGetRoot();
	entSetRoot(NULL);
	
	return entRoot;
}

void cFileNameToCppFileName(char* fileName)
{
	char* ptrstr;
	char bufstr[SIZE_FILE_NAME];
	
	//If source file ?
	if(strstr(fileName, ".c"))
		strcat(fileName, "pp");
	
	ptrstr = strstr(fileName, "/c/");
	strcpy(bufstr, ptrstr+3);
	*ptrstr='\0';
	strcat(fileName, "/cpp/");
	strcat(fileName, bufstr);
}

void writeHeaderArmPortClass(entity_t* treeHeader, entity_t* treeSource, int fd)
{
	entity_t* ent = treeHeader;
	bool inClassPublic = false;
	bool inClassStop = false;
	
	//Write header
	dprintf(fd, HERDER_FILE);
	dprintf(fd, LICENCE);
	while(ent)
	{
		if(ent->nextEntity == NULL)
		{
			inClassPublic = false;
			inClassStop = true;
		}
		
		//Start to write class, write public functions
		if(	inClassPublic ||
			ent->type == ENT_FUN_PROTO_ARM_PORT			||
			ent->type == ENT_FUN_STATIC_PROTO_ARM_PORT)
		{
			if(!inClassPublic)
			{
				dprintf(fd, "\nclass ArmPort\n");
				dprintf(fd, "{\n");
				dprintf(fd, "\tpublic:\n");
				dprintf(fd, "\t\tArmPort();\n");
				dprintf(fd, "\t\t~ArmPort();\n\n\t\t");
				
				inClassPublic = true;
			}
			else
			{
				dprintf(fd, "\t\t");
			}
		}
		else if(inClassStop)//write end class
		{	
			//Write private attributes
			dprintf(fd, "\n\tprivate:\n");
			dprintf(fd, "\tvoid* _port;\n");
			dprintf(fd, "};\n\n");
			
			inClassStop = false;
			inClassPublic = false;
		}
		
		if(ent->type == ENT_DOC)
			ent->type = ENT_DOC_ARM_PORT;
			
		entCToCpp(ent);
		entWrite(fd, ent);
		ent = ent->nextEntity;
	}
}

void writeHeaderArmN8Class(entity_t* treeHeader, entity_t* treeSource, int fd)
{
	entity_t* ent = treeHeader;
	bool inClassPublic = false;
	bool inClassStop = false;
	entity_t* entArmStruc = NULL;
	
	//Write header
	dprintf(fd, HERDER_FILE);
	dprintf(fd, LICENCE);
	while(ent)
	{
		if(ent->nextEntity == NULL)
		{
			inClassPublic = false;
			inClassStop = true;
		}
			
		if(ent->type == ENT_STRUCT)
		{
			//Is armN8_t structure ?
			if(strstr(ent->str, "}armN8_t;"))
			{
				entArmStruc = ent;
				ent = ent->nextEntity;
				continue;
			}
		}
		
		//Start to write class, write public functions
		if(	inClassPublic ||
			ent->type == ENT_FUN_PROTO_ARM			||
			ent->type == ENT_FUN_STATIC_PROTO_ARM)
		{
			if(!inClassPublic)
			{
				//dprintf(fd, "class ArmPort;\n");
				dprintf(fd, "class ArmN8\n");
				dprintf(fd, "{\n");
				dprintf(fd, "\tpublic:\n");
				dprintf(fd, "\t\tArmN8();\n");
				dprintf(fd, "\t\t~ArmN8();\n\n\t\t");
				
				inClassPublic = true;
			}
			else
			{
				dprintf(fd, "\t\t");
			}
		}
		else if(inClassStop)//write end class
		{
			//write private functions from source file
			dprintf(fd, "\n\tprivate:\n");
			entity_t* ent = treeSource;
			while(ent)
			{
				if(	ent->type == ENT__FUN_PROTO_ARM			||
					ent->type == ENT__FUN_STATIC_PROTO_ARM)
				{
					entCToCpp(ent);
					dprintf(fd, "\t\t");
					entWrite(fd, ent);
				}
				
				ent = ent->nextEntity;
			}
			
			//Write private attributes
			dprintf(fd, "\n\tArmPort\t_port;\n");
			
			//Extract attributes from armN8_t structure
			char cstr[1024];
			strcpy(cstr, entArmStruc->str);
			strRepExp(cstr, "[^\n]+\n", "");
			strRepExp(cstr, "[^\n]+\n", "");
			strRepExp(cstr, "[^\n]+\n", "");
			strRepExp(cstr, "}armN8_t;", "");
			
			dprintf(fd, "%s", cstr);
			
			//End to write class
			dprintf(fd, "};\n\n");
			
			inClassStop = false;
			inClassPublic = false;
		}
		
		if(ent->type == ENT_DOC)
			ent->type = ENT_DOC_ARM_N8;
			
		entCToCpp(ent);
		entWrite(fd, ent);
		ent = ent->nextEntity;
	}
}

void writeSourceArmN8Class(entity_t* treeHeader, entity_t* treeSource, int fd)
{
	bool writConstructor = false;
	entity_t* ent = treeSource;
	
	//Write header
	dprintf(fd, HERDER_FILE);
	dprintf(fd, LICENCE);
	while(ent)
	{	
		if(	ent->type == ENT__FUN_PROTO_ARM ||
			ent->type == ENT__FUN_STATIC_PROTO_ARM)
		{
			ent = ent->nextEntity;
			continue;
		}
		
		if(	!writConstructor &&
			(ent->type == ENT_FUNCTION))
		{
			dprintf(fd, "ArmN8::ArmN8()\n");
			dprintf(fd, "{\n");
			dprintf(fd, "}\n\n");

			dprintf(fd, "ArmN8::~ArmN8()\n");
			dprintf(fd, "{\n");
			dprintf(fd, "}\n\n");
			
			writConstructor = true;
		}
		
		if(ent->type == ENT_DOC)
			ent->type = ENT_DOC_ARM_N8;
			
		entCToCpp(ent);
		entWrite(fd, ent);
		ent = ent->nextEntity;
	}
}
